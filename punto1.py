# Explorando range()

# Lista del 0 al 4
lista1 = list(range(5))
print("list(range(5)):", lista1)

# Lista del 1 al 5
lista2 = list(range(1, 6))
print("list(range(1, 6)):", lista2)

# Lista del 0 al 8 con paso de 2
lista3 = list(range(0, 10, 2))
print("list(range(0, 10, 2)):", lista3)
